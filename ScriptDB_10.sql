CREATE TABLE KY_TUC_XA
(
	MA_KTX VARCHAR(6) PRIMARY KEY, 
	TEN_KTX NVARCHAR(150) NOT NULL, 
	DIA_CHI NVARCHAR(500) NOT NULL, 
