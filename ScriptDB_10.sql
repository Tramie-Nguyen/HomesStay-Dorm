CREATE TABLE PHONG
(
	MA_PHONG VARCHAR(4) PRIMARY KEY, 
	IMAGE_URL VARCHAR(255), 
	SL_GIUONG INT CHECK (SL_GIUONG > 0),
	SL_GIUONG_TRONG INT CHECK (SL_GIUONG_TRONG >= 0),
	TRANG_THAI NVARCHAR(10) CHECK (TRANG_THAI IN (N'Đã thuê', N'Trống')),
	MA_KTX VARCHAR(6),
	CONSTRAINT FK_PHONG_MAKTX FOREIGN KEY (MA_KTX) REFERENCES KY_TUC_XA(MA_KTX),
	CONSTRAINT CHK_PHONG_SLT CHECK (SL_GIUONG_TRONG <= SL_GIUONG)
