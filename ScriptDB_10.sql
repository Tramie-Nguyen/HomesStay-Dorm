/* =========================================================
   2. KTX → PHÒNG → GIƯỜNG
========================================================= */

CREATE TABLE KY_TUC_XA
(
    MA_KTX VARCHAR(6) PRIMARY KEY,
    TEN_KTX NVARCHAR(150) NOT NULL,
    DIA_CHI NVARCHAR(500) NOT NULL,
    QUY_DINH NVARCHAR(500),
    MA_DIEU_KHOAN VARCHAR(5),

    GIA_DIEN INT,
    GIA_NUOC INT,
    WIFI NVARCHAR(50),
    GUI_XE INT,
    DICH_VU INT,

    CONSTRAINT FK_KTX_MADIEUKHOAN
        FOREIGN KEY (MA_DIEU_KHOAN)
        REFERENCES CHINH_SACH_THUE(MA_DIEU_KHOAN)
);

CREATE TABLE PHONG
(
    MA_KTX VARCHAR(6),
    MA_PHONG VARCHAR(10),
    IMAGE_URL VARCHAR(255),

    SL_GIUONG INT CHECK (SL_GIUONG > 0),
    SL_GIUONG_TRONG INT CHECK (SL_GIUONG_TRONG >= 0),
    TRANG_THAI NVARCHAR(10)
        CHECK (TRANG_THAI IN (N'Đã thuê', N'Trống')),

    SO_DIEN_CU INT CHECK (SO_DIEN_CU >= 0),
    SO_DIEN_MOI INT CHECK (SO_DIEN_MOI >= 0),

    MO_TA NVARCHAR(255),

    PRIMARY KEY (MA_KTX, MA_PHONG),

    CONSTRAINT FK_PHONG_MAKTX
        FOREIGN KEY (MA_KTX)
        REFERENCES KY_TUC_XA(MA_KTX),

    CONSTRAINT CHK_PHONG_SLT
        CHECK (SL_GIUONG_TRONG <= SL_GIUONG),

    CONSTRAINT CHK_PHONG_SODIEN
        CHECK (SO_DIEN_MOI >= SO_DIEN_CU)
);

CREATE TABLE GIUONG
(
    MA_KTX VARCHAR(6),
    MA_PHONG VARCHAR(10),
    MA_GIUONG VARCHAR(10),

    TRANG_THAI NVARCHAR(10)
        CHECK (TRANG_THAI IN (N'Đã thuê', N'Trống')),

    GIA INT CHECK (GIA > 0),

    PRIMARY KEY (MA_KTX, MA_PHONG, MA_GIUONG),

    CONSTRAINT FK_GIUONG_MAPHONG
        FOREIGN KEY (MA_KTX, MA_PHONG)
        REFERENCES PHONG(MA_KTX, MA_PHONG)
);

