--số lượng người thuê <= tổng số lượng giường của phòng
CREATE TRIGGER TRG_Check_SL_NguoiThue
ON HOP_DONG_THUE
AFTER INSERT, UPDATE
AS
BEGIN
    IF EXISTS (
        SELECT 1
        FROM inserted i
        JOIN LICH l ON i.MA_PHIEU = l.MA_PHIEU
        JOIN PHONG p ON l.MA_PHONG = p.MA_PHONG
        WHERE i.SL_NGUOI_THUE > p.SL_GIUONG
    )
    BEGIN
        RAISERROR (N'Lỗi: Số lượng người thuê vượt quá tổng số giường của phòng!', 16, 1);
        ROLLBACK TRANSACTION;
    END
END;
GO

