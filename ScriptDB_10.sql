	CONSTRAINT FK_PDKT_MANV FOREIGN KEY (MA_NV) REFERENCES NHAN_VIEN(MA_NV),
	CONSTRAINT FK_PDKT_MAKH FOREIGN KEY (MA_KH) REFERENCES KHACH_HANG(MA_KH),
);

CREATE TABLE PHIEU_DAT_COC
(
