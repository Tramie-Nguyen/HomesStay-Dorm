CREATE TABLE BIEN_BAN_TRA_PHONG
(
	MA_BB_TRA_PHONG VARCHAR(5) PRIMARY KEY, 
	NGAY DATE CHECK (NGAY <= GETDATE()),
	NOI_DUNG NVARCHAR(500),
	MA_NV VARCHAR(5),
	MA_THANH_TOAN VARCHAR(5), 
	MA_HOP_DONG VARCHAR(5),
	TRANG_THAI NVARCHAR(20) CHECK (TRANG_THAI IN (N'Đã hẹn', N'Đang chờ xử lí', N'Đã xử lí')),
	CONSTRAINT FK_BIENBANTRAPHONG_MANV FOREIGN KEY (MA_NV) REFERENCES NHAN_VIEN(MA_NV),
	CONSTRAINT FK_BIENBANTRAPHONG_MATHANHTOAN FOREIGN KEY (MA_THANH_TOAN) REFERENCES PHIEU_THANH_TOAN(MA_THANH_TOAN),
	CONSTRAINT FK_BIENBANTRAPHONG_MAHOPDONG FOREIGN KEY (MA_HOP_DONG) REFERENCES HOP_DONG_THUE(MA_HOP_DONG)
