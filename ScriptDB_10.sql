	);

CREATE TABLE NHAN_VIEN
(
    MA_NV VARCHAR(5) PRIMARY KEY,
    TEN_NV NVARCHAR(250) NOT NULL,

    NGAY_SINH DATE CHECK (NGAY_SINH < CAST(GETDATE() AS DATE)),
