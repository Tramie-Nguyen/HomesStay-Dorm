	MA_KH VARCHAR(5),
	MA_NV VARCHAR(5),
	CONSTRAINT FK_PDC_MANV FOREIGN KEY (MA_NV) REFERENCES NHAN_VIEN(MA_NV),
	CONSTRAINT FK_PDC_MAKH FOREIGN KEY (MA_KH) REFERENCES KHACH_HANG(MA_KH)
